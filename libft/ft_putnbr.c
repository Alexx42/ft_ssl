#include "libft.h"

int	ft_putnbr(int n)
{
	int len;

	len = 0;
	if (n == -2147483648)
		len += ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			len += ft_putchar('-');
			n *= -1;
		}
		if (n >= 10)
			len += ft_putnbr(n / 10);
		len += ft_putchar((n % 10) + '0');
	}
	return (len);
}
